/**
 * Option 1 — Single-host scale-out.
 * N gateway containers, one Postgres catalog, one bind-mounted data directory.
 * No external networking required.
 */

import { prompt, promptInt, promptYN, promptRequired, randomToken, c } from './prompts.js';
import { ensureOutputDir, writeOutput, renderEnv } from './util.js';

export default async function run() {
    console.log(c.dim(
        'All gateway instances run on this host and share one Postgres catalog\n' +
        'plus one local data directory. Lightweight, zero networking glue.\n'
    ));

    const n         = await promptInt('How many gateway instances?', 3, { min: 1, max: 32 });
    const basePort  = await promptInt('Base host port (gateways will use base, base+1, ...)', 3333, { min: 1024, max: 60000 });
    const includeKafka = await promptYN('Include the Kafka broker service?', true);
    const apiKeyInput = await prompt(
        'API key(s), comma-separated (blank = generate one)',
        ''
    );
    const apiKeys   = apiKeyInput.trim() || `ICHIBI_LAKE_${randomToken(16)}`;
    const sqlRO     = await promptYN('Enforce SQL_READ_ONLY on user-supplied SQL endpoints?', true);

    const outDir = await ensureOutputDir('option1-singlehost');

    // ── compose.yaml ────────────────────────────────────────────────────────
    const appServices = Array.from({ length: n }, (_, i) => {
        const idx  = i + 1;
        const port = basePort + i;
        return `  app-${idx}:
    <<: *app-base
    container_name: superlake-app-${idx}
    ports: ["${port}:3000"]`;
    }).join('\n\n');

    const kafkaService = includeKafka ? `
  kafka:
    image: apache/kafka:4.3.1
    container_name: superlake-kafka
    environment:
      KAFKA_PROCESS_ROLES: broker,controller
      KAFKA_NODE_ID: 1
      KAFKA_CONTROLLER_QUORUM_VOTERS: 1@localhost:9093
      KAFKA_LISTENERS: PLAINTEXT://0.0.0.0:9092,CONTROLLER://0.0.0.0:9093
      KAFKA_ADVERTISED_LISTENERS: PLAINTEXT://kafka:9092
      KAFKA_LISTENER_SECURITY_PROTOCOL_MAP: PLAINTEXT:PLAINTEXT,CONTROLLER:PLAINTEXT
      KAFKA_CONTROLLER_LISTENER_NAMES: CONTROLLER
      KAFKA_INTER_BROKER_LISTENER_NAME: PLAINTEXT
      KAFKA_OFFSETS_TOPIC_REPLICATION_FACTOR: 1
      KAFKA_TRANSACTION_STATE_LOG_REPLICATION_FACTOR: 1
      KAFKA_TRANSACTION_STATE_LOG_MIN_ISR: 1
      CLUSTER_ID: superlake-cluster
    networks: [app-network]
    healthcheck:
      test: ["CMD-SHELL", "/opt/kafka/bin/kafka-topics.sh --bootstrap-server localhost:9092 --list"]
      interval: 10s
      timeout: 5s
      retries: 10
      start_period: 30s
    restart: unless-stopped
` : '';

    const kafkaDep = includeKafka ? `
      kafka:
        condition: service_healthy` : '';
    const kafkaEnv = includeKafka ? '\n      - KAFKA_BROKERS=kafka:9092' : '\n      - KAFKA_BROKERS=';

    const compose = `# superlake/${outDir.rel.split('/').pop()}/compose.yaml
# Generated by scripts/superlake-wizard.js
# Topology: Option 1 — Single-host scale-out (${n} gateways, 1 catalog, 1 bind-mount).

x-app-base: &app-base
  build:
    context: ../..
    dockerfile: docker/Dockerfile
  environment:
      - DUCKLAKE_PG_HOST=db
      - DUCKLAKE_PG_PORT=5432
      - DUCKLAKE_PG_USER=postgres
      - DUCKLAKE_PG_PASSWORD=postgres
      - DUCKLAKE_PG_DB=ducklake
      - DUCKLAKE_DATA_PATH=/app/data_files
      - DUCKDB_ALLOW_COMMUNITY_EXTENSIONS=1
      - USE_FILESYSTEM_BLOBS=1
      - BLOBS_PATH=/app/blobs
      - API_KEYS=\${API_KEYS}
      - SQL_READ_ONLY=\${SQL_READ_ONLY:-true}
      - PORT=3000${kafkaEnv}
  volumes:
      - ./data/ducklake:/app/data_files
      - ./data/blobs:/app/blobs
  networks: [app-network]
  depends_on:
      db:
        condition: service_healthy${kafkaDep}
  healthcheck:
      test: ["CMD-SHELL", "curl -sf http://localhost:3000/swagger.yaml > /dev/null"]
      interval: 10s
      timeout: 5s
      retries: 5
      start_period: 30s
  restart: unless-stopped

services:
  db:
    image: postgres:17
    container_name: superlake-postgres
    environment:
      - POSTGRES_USER=postgres
      - POSTGRES_PASSWORD=postgres
      - POSTGRES_DB=ducklake
    volumes:
      - ./data/postgres:/var/lib/postgresql/data
    networks: [app-network]
    healthcheck:
      test: ["CMD-SHELL", "pg_isready -U postgres"]
      interval: 5s
      timeout: 5s
      retries: 5
      start_period: 10s
    restart: unless-stopped
${kafkaService}
${appServices}

networks:
  app-network:
    driver: bridge
`;

    const envBody = renderEnv(
        { API_KEYS: apiKeys, SQL_READ_ONLY: String(sqlRO) },
        {
            API_KEYS:      'Comma-separated API keys accepted by every gateway',
            SQL_READ_ONLY: 'Reject DDL/DML on /query, /graphql, /graphs/:n/query',
        },
        `SuperLake — Option 1 (single-host scale-out, ${n} gateways)`,
    );

    const ports = Array.from({ length: n }, (_, i) => `http://localhost:${basePort + i}`);
    const readme = `# SuperLake — Option 1: Single-host scale-out

${n} ichibi-lake gateway containers share **one Postgres catalog** and **one bind-mounted
data directory**. All ${n} gateways read and write the same DuckLake.

## What was generated

| File | Purpose |
|---|---|
| \`compose.yaml\` | ${n} \`app-*\` services + \`db\`${includeKafka ? ' + `kafka`' : ''} |
| \`.env\` | API keys and SQL_READ_ONLY toggle |

## Quick start

\`\`\`bash
cd ${outDir.rel}
docker compose up -d --build
\`\`\`

Gateways will be reachable at:

${ports.map(u => `- ${u}`).join('\n')}

## Verify the lake is shared

\`\`\`bash
# Insert via gateway 1
curl -X POST ${ports[0]}/upload/shared_demo \\
  -H 'Content-Type: application/json' \\
  -H "x-api-key: $(grep -E '^API_KEYS=' .env | cut -d= -f2 | cut -d, -f1 | tr -d '"')" \\
  -d '[{"id":"a","note":"hello from app-1"}]'

# Read via gateway ${n > 1 ? 2 : 1}
curl ${ports[n > 1 ? 1 : 0]}/tables/shared_demo \\
  -H "x-api-key: $(grep -E '^API_KEYS=' .env | cut -d= -f2 | cut -d, -f1 | tr -d '"')"
\`\`\`

If both gateways see the row, the lake is shared.

## Notes

- The build context points at \`../..\` so the existing \`docker/Dockerfile\` is reused.
- Data persists under \`${outDir.rel}/data/{ducklake,blobs,postgres}\`.
- DuckLake handles concurrent writers via optimistic concurrency on \`snapshot_id\`.
  For heavy parallel write workloads consider \`FORCE INSTALL ducklake FROM core_nightly\`.
`;

    const files = [];
    files.push(await writeOutput(outDir, 'compose.yaml', compose));
    files.push(await writeOutput(outDir, '.env',         envBody));
    files.push(await writeOutput(outDir, 'README.md',    readme));

    return {
        outDir,
        files,
        nextSteps: [
            `cd ${outDir.rel}`,
            'docker compose up -d --build',
            `Then hit any of ${ports.join(', ')}`,
        ],
    };
}
