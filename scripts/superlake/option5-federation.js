/**
 * Option 5 — Federation.
 * Each host runs an independent DuckLake (own Postgres + own data dir).
 * Gateways then ATTACH their peers' catalogs read-only at startup, so a
 * single query can SELECT across all lakes (e.g. UNION ALL across peers).
 *
 * No shared filesystem, no shared catalog — only cross-attach for queries.
 * Writes always go to the local lake; reads can span the federation.
 */

import { prompt, promptInt, promptYN, promptRequired, promptList, randomToken, c } from './prompts.js';
import { ensureOutputDir, writeOutput, renderEnv, detectLanIp } from './util.js';

export default async function run() {
    console.log(c.dim(
        'Federation topology. Every host has its own Postgres + data dir, and\n' +
        'every gateway ATTACHes the OTHER hosts\' catalogs as read-only peers.\n' +
        'Local writes; federated reads. No shared storage required.\n'
    ));

    const lanIp     = await detectLanIp();
    const selfIp    = await promptRequired('LAN IP of THIS host', lanIp || '');
    const selfName  = await promptRequired('Logical name for THIS lake (used as catalog alias)', 'lake_local');
    const hostPort  = await promptInt('Host port for the gateway', 3333, { min: 1024, max: 60000 });

    console.log(c.dim(
        '\nEnter peer lakes. Format per entry: name@host:port\n' +
        'e.g. lake_dubai@10.0.0.11:5432,lake_riyadh@10.0.0.12:5432\n'
    ));
    const peerSpecs = await promptList('Peer lakes (comma-separated, blank if none)', '');
    const peers = peerSpecs.map(parsePeerSpec).filter(Boolean);
    if (peers.length === 0) {
        console.log(c.yellow(
            'No peers entered — generating a standalone lake. You can re-run\n' +
            'this wizard later once peers are reachable.'
        ));
    }

    const apiKeyInput = await prompt('API key(s), comma-separated (blank = generate one)', '');
    const apiKeys     = apiKeyInput.trim() || `ICHIBI_LAKE_${randomToken(16)}`;
    const sqlRO       = await promptYN('Enforce SQL_READ_ONLY on user-supplied SQL endpoints?', true);

    const outDir = await ensureOutputDir(`option5-federation-${selfName}`);

    const peersEnv = peers.map(p => `${p.name}@${p.host}:${p.port}`).join(',');

    const compose = `# superlake/${outDir.rel.split('/').pop()}/compose.yaml
# Generated by scripts/superlake-wizard.js
# Topology: Option 5 — Federation, host = ${selfName} @ ${selfIp}
#
# This stack is fully self-contained: its own Postgres, its own data dir.
# Peer lakes are attached at runtime via the db-federation-patch.md changes
# in src/db.js — see that file for the small addition required.

services:
  db:
    image: postgres:17
    container_name: superlake-${selfName}-postgres
    ports:
      - "5432:5432"   # exposed so peers can ATTACH this catalog read-only
    environment:
      - POSTGRES_USER=postgres
      - POSTGRES_PASSWORD=postgres
      - POSTGRES_DB=ducklake
    volumes:
      - ./data/postgres:/var/lib/postgresql/data
    networks: [app-network]
    healthcheck:
      test: ["CMD-SHELL", "pg_isready -U postgres"]
      interval: 5s
      timeout: 5s
      retries: 5
    restart: unless-stopped

  app:
    build:
      context: ../..
      dockerfile: docker/Dockerfile
    container_name: superlake-${selfName}-app
    ports: ["${hostPort}:3000"]
    environment:
      - DUCKLAKE_PG_HOST=db
      - DUCKLAKE_PG_PORT=5432
      - DUCKLAKE_PG_USER=postgres
      - DUCKLAKE_PG_PASSWORD=postgres
      - DUCKLAKE_PG_DB=ducklake
      - DUCKLAKE_DATA_PATH=/app/data_files
      - DUCKDB_ALLOW_COMMUNITY_EXTENSIONS=1
      - USE_FILESYSTEM_BLOBS=1
      - BLOBS_PATH=/app/blobs
      - API_KEYS=\${API_KEYS}
      - SQL_READ_ONLY=\${SQL_READ_ONLY:-true}
      - PORT=3000
      # Consumed by the db-federation-patch.md changes in src/db.js
      - DUCKLAKE_LOCAL_NAME=${selfName}
      - DUCKLAKE_PEERS=\${DUCKLAKE_PEERS}
    volumes:
      - ./data/ducklake:/app/data_files
      - ./data/blobs:/app/blobs
    networks: [app-network]
    depends_on:
      db:
        condition: service_healthy
    healthcheck:
      test: ["CMD-SHELL", "curl -sf http://localhost:3000/swagger.yaml > /dev/null"]
      interval: 10s
      timeout: 5s
      retries: 5
      start_period: 30s
    restart: unless-stopped

networks:
  app-network:
    driver: bridge
`;

    const envBody = renderEnv(
        {
            API_KEYS:       apiKeys,
            SQL_READ_ONLY:  String(sqlRO),
            DUCKLAKE_PEERS: peersEnv,
        },
        {
            DUCKLAKE_PEERS: 'Comma-separated list of name@host:port peer lake catalogs',
        },
        `SuperLake — Option 5 (Federation), host = ${selfName} @ ${selfIp}`,
    );

    const files = [];
    files.push(await writeOutput(outDir, 'compose.yaml',           compose));
    files.push(await writeOutput(outDir, '.env',                   envBody));
    files.push(await writeOutput(outDir, 'README.md',              renderReadme(selfName, selfIp, hostPort, peers, outDir)));
    files.push(await writeOutput(outDir, 'db-federation-patch.md', federationPatchDoc()));

    return {
        outDir, files,
        nextSteps: [
            `Apply db-federation-patch.md to src/db.js (if not already done)`,
            `cd ${outDir.rel}`,
            'docker compose up -d --build',
            `Then hit http://localhost:${hostPort}`,
            peers.length
                ? `Peers attached at startup: ${peers.map(p => p.name).join(', ')}`
                : 'No peers configured yet — re-run the wizard once peers exist',
        ],
    };
}

function parsePeerSpec(spec) {
    const m = spec.match(/^([A-Za-z_][A-Za-z0-9_]*)@([^:]+):(\d+)$/);
    if (!m) {
        console.log(c.red(`  ↳ ignoring malformed peer "${spec}" (expected name@host:port)`));
        return null;
    }
    return { name: m[1], host: m[2], port: parseInt(m[3], 10) };
}

function renderReadme(selfName, selfIp, hostPort, peers, outDir) {
    const peerRows = peers.length
        ? peers.map(p => `| \`${p.name}\` | \`${p.host}:${p.port}\` |`).join('\n')
        : '| _(none — standalone)_ | — |';
    return `# SuperLake — Option 5 (Federation): ${selfName}

This host runs a fully independent DuckLake stack (own Postgres, own data
dir) and ATTACHes peer lakes read-only at gateway startup. Writes always
land in **${selfName}**; reads can span all attached peers.

| Local lake | Address |
|---|---|
| \`${selfName}\` | \`${selfIp}:5432\` |

## Peer lakes

| Peer name | Address |
|---|---|
${peerRows}

## Quick start

\`\`\`bash
# 1. Apply db-federation-patch.md to src/db.js (one-time, all hosts).
cd ${outDir.rel}
docker compose up -d --build
\`\`\`

Gateway URL: http://localhost:${hostPort}

## Querying across the federation

After applying the patch, each peer is exposed as its own DuckDB catalog,
so cross-lake SQL works directly:

\`\`\`sql
SELECT * FROM ${selfName}.main.orders
UNION ALL
${peers.map(p => `SELECT * FROM ${p.name}.main.orders`).join('\nUNION ALL\n') || '-- (no peers configured)'};
\`\`\`

## Notes

- Set \`SQL_READ_ONLY=true\` (default) to prevent accidental cross-lake DML.
- Peers do NOT need to share an API key — the cross-attach is over the raw
  Postgres protocol (catalog only). Parquet data files remain local to each
  host; remote reads will fail unless those files are also reachable.
  For pure metadata queries (table list, schemas) this works as-is.
`;
}

function federationPatchDoc() {
    return `# Required patch to src/db.js for cross-lake federation

Adds a startup loop that ATTACHes every peer lake listed in
\`DUCKLAKE_PEERS\` as a read-only DuckDB catalog. After applying, a
single query can SELECT across all peers (subject to data-file reachability).

Insert immediately AFTER the existing
\`await conn.run("USE ducklake;");\` line in \`src/db.js\`:

\`\`\`js
// SuperLake / Federation: attach peer lakes read-only.
const peersSpec = (process.env.DUCKLAKE_PEERS || '').trim();
if (peersSpec) {
    for (const raw of peersSpec.split(',').map(s => s.trim()).filter(Boolean)) {
        const m = raw.match(/^([A-Za-z_][A-Za-z0-9_]*)@([^:]+):(\\d+)$/);
        if (!m) { console.warn('[DB] Ignoring malformed peer:', raw); continue; }
        const [, name, host, port] = m;
        try {
            // Each peer gets its own ducklake SECRET + ATTACH alias.
            const peerMeta = \`postgres:host=\${host} port=\${port} user=\${pgUser} password=\${pgPass} dbname=\${pgDb}\`;
            await conn.run(\`CREATE SECRET sec_\${name} (TYPE ducklake, METADATA_PATH '\${peerMeta}');\`);
            await conn.run(\`ATTACH 'ducklake:sec_\${name}' AS \${name} (READ_ONLY);\`);
            console.log('[DB] Attached peer lake:', name, '@', host + ':' + port);
        } catch (e) {
            console.warn('[DB] Peer attach failed for', name, '-', e.message || e);
        }
    }
}
\`\`\`

Cross-lake query example after the patch:

\`\`\`sql
SELECT 'local' AS src, count(*) FROM ducklake.main.orders
UNION ALL
SELECT 'peer1', count(*) FROM lake_dubai.main.orders;
\`\`\`
`;
}
